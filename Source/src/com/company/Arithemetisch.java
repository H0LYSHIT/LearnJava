package com.company;

public class Arithemetisch {

    public static void main (String [] args){

        int swag = 10;

        int yolo = 5;

        int ergebnis;

        ergebnis = swag + yolo;

        System.out.print("Das Ergebnis von swag + yolo ist =");
        System.out.println(ergebnis);

        ergebnis = swag - yolo;

        System.out.print("Das Ergebnis von swag - yolo ist =");
        System.out.println(ergebnis);

        ergebnis = swag * yolo;

        System.out.print("Das Ergebnis von swag * yolo ist =");
        System.out.println(ergebnis);

        ergebnis = swag / yolo;

        System.out.print("Das Ergebnis von swag / yolo ist =");
        System.out.println(ergebnis);


    }
}
